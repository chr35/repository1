package com.ems.entity;
/**
 * 课程信息
 * @author hrcui on 2017-5-11
 */
public class Course {
	private String id;
	private String courseid;
	private String coursename;
	private String type;
	private String credit;
	private String creditType;
	private String courseTypr;
	private String bz;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCourseid() {
		return courseid;
	}
	public void setCourseid(String courseid) {
		this.courseid = courseid;
	}
	public String getCoursename() {
		return coursename;
	}
	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCredit() {
		return credit;
	}
	public void setCredit(String credit) {
		this.credit = credit;
	}
	public String getCreditType() {
		return creditType;
	}
	public void setCreditType(String creditType) {
		this.creditType = creditType;
	}
	public String getCourseTypr() {
		return courseTypr;
	}
	public void setCourseTypr(String courseTypr) {
		this.courseTypr = courseTypr;
	}
	public String getBz() {
		return bz;
	}
	public void setBz(String bz) {
		this.bz = bz;
	}
	@Override
	public String toString() {
		return "Course [id=" + id + ", courseid=" + courseid + ", coursename="
				+ coursename + ", type=" + type + ", credit=" + credit
				+ ", creditType=" + creditType + ", courseTypr=" + courseTypr
				+ ", bz=" + bz + "]";
	}
	
}
